const files = [
  ...fs.readdirSync('public').filter(f => f.endsWith('.html') || f.endsWith('.js')).map(f => `public/${f}`),
  ...fs.readdirSync('public/games').filter(f => f.endsWith('.html')).map(f => `public/games/${f}`),
  'server.js', 'credits.js'
];

let totalFixed = 0;
files.forEach(filePath => {
  if (!fs.existsSync(filePath)) return;
  let content = fs.readFileSync(filePath, 'utf8');
  const original = content;
  fixes.forEach(([bad, good]) => {
    content = content.split(bad).join(good);
  });
  if (content !== original) {
    fs.writeFileSync(filePath, content, 'utf8');
    totalFixed++;
    console.log(`Fixed: ${filePath}`);
  }
});
console.log(`\nTotal: ${totalFixed} archivos arreglados`);
